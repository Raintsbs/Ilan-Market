const bundles: Record<LocaleCode, Record<MessageKey, string>> = {
  tr,
  en,
  de,
  fr,
  es,
  it,
  pt,
  ru,
  ar,
  zh,
  ja,
  ko,
};

export function translate(locale: LocaleCode, key: MessageKey): string {
  return bundles[locale]?.[key] ?? bundles.en[key] ?? tr[key] ?? key;
}

/** {count} gibi yer tutucuları değiştirir */
export function tFormat(
  locale: LocaleCode,
  key: MessageKey,
  vars?: Record<string, string | number>,
): string {
  let text = translate(locale, key);
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}
